class I18n {
  constructor(settings) {
    this._s = settings;
    const saved = settings.get('lang');
    this._lang = saved || this._detectLang();
    if (!saved) settings.set('lang', this._lang);
  }

  _detectLang() {
    const supported = ['uk', 'en', 'fr', 'de', 'pl', 'cs', 'es', 'pt'];
    const prefs = navigator.languages?.length ? navigator.languages : [navigator.language];
    for (const l of prefs) {
      const code = l.split('-')[0].toLowerCase();
      if (supported.includes(code)) return code;
    }
    return 'en';
  }

  get lang() { return this._lang; }

  t(key) {
    return (TRANSLATIONS[this._lang] ?? TRANSLATIONS.uk)[key]
        ?? TRANSLATIONS.uk[key]
        ?? key;
  }

  setLang(lang) {
    if (!TRANSLATIONS[lang]) return;
    this._lang = lang;
    this._s.set('lang', lang);
    document.documentElement.lang = lang;
    this._applyToDOM();
  }

  _applyToDOM() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.textContent = this.t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      el.innerHTML = this.t(el.dataset.i18nHtml);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = this.t(el.dataset.i18nPlaceholder);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = this.t(el.dataset.i18nTitle);
    });
  }
}
